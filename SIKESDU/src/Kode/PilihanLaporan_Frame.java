    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
        // TODO add your handling code here:
        if(tanggal_awal.getDate()==null || tanggal_akhir.getDate()==null || combo_jenis.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, 
            "Seluruh Kolom Harus Terisi!!!" , 
            "Informasi Cetak Laporan" , 
            JOptionPane.INFORMATION_MESSAGE);
        }else{
            tanggalawal = String.valueOf(fm.format(tanggal_awal.getDate()));
            tanggalakhir = String.valueOf(fm.format(tanggal_akhir.getDate()));
            if(combo_jenis.getSelectedItem().equals("Perkembangan Ibu Hamil")){
                cekDataBumil();
            }else if(combo_jenis.getSelectedItem().equals("Perkembangan Balita")){
                cekDataBalita();
            }
        }
    }//GEN-LAST:event_btn_cetakActionPerformed

