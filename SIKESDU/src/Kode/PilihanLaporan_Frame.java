    private void cetakLaporan(){
        try{
            JasperReport jr = JasperCompileManager.compileReport(reportpath);
            HashMap parameter = new HashMap();
            parameter.put("tgl_awal", tanggalawal);
            parameter.put("tgl_akhir", tanggalakhir);
            JasperPrint jp = JasperFillManager.fillReport(jr, parameter, con);
            JasperViewer.viewReport(jp, false);
            reset();
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void reset(){
        tanggal_awal.setDate(null);
        tanggal_akhir.setDate(null);
        combo_jenis.setSelectedIndex(0);
        reportpath = "";
        tanggalawal = "";
        tanggalakhir = "";
        sql = "";
    }
    
    private void cekDataBumil(){
        try{
            sql = "SELECT COUNT(*) AS jumlah FROM `perkembangan_ibuhamil` "
                + "INNER JOIN `data_ibuhamil` ON perkembangan_ibuhamil.nik_ibuhamil = "
                + "data_ibuhamil.nik_ibuhamil WHERE tgl_periksa "
                + "BETWEEN '"+tanggalawal+"' and '"+tanggalakhir+"';";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
                if(rs.getString("jumlah").equals("0")){
                    JOptionPane.showMessageDialog(this, 
                    "Maaf Data Kosong! \nPastikan Seluruh Kolom Terisi dengan Benar" , 
                    "Informasi Cetak Laporan" , 
                    JOptionPane.INFORMATION_MESSAGE);
                }else{
                    reportpath = "src\\Kode\\ReportBumil.jrxml";
                    cetakLaporan();
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void cekDataBalita(){
        try{
            sql = "SELECT COUNT(*) as jumlah FROM `perkembangan_balita` "
                + "INNER JOIN `data_balita` ON perkembangan_balita.id_balita = "
                + "data_balita.id_balita WHERE tgl_periksa "
                + "BETWEEN '"+tanggalawal+"' and '"+tanggalakhir+"';";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
                if(rs.getString("jumlah").equals("0")){
                    JOptionPane.showMessageDialog(this, 
                    "Maaf Data Kosong! \nPastikan Seluruh Kolom Terisi dengan Benar" , 
                    "Informasi Cetak Laporan" , 
                    JOptionPane.INFORMATION_MESSAGE);
                }else{
                    reportpath = "src\\Kode\\ReportBalita.jrxml";
                    cetakLaporan();
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }

